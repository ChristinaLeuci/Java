public class Slash{
  public static void main(String[] args){
    first();
    second();
    third();
    fourth();
    fifth();
    sixth();
  }
  public static void first(){
    System.out.println("  \\/");
  }
  public static void second(){
    System.out.println(" \\\\//");
  }
  public static void third(){
    System.out.println("\\\\\\///");
  }
  public static void fourth(){
    System.out.println("///\\\\\\");
  }
  public static void fifth(){
    System.out.println(" //\\\\");
  }
  public static void sixth(){
    System.out.println("  /\\");  
  }
}